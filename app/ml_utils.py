def generate_text(prompt: str, tariff: str) -> str:
    """Простая генерация текста на основе промпта и тарифа"""
    template = {
        "standart": "Краткий ответ: {prompt} - это отличный продукт!",
        "pro": "Средний ответ: {prompt} - это современное решение, которое поможет вам достичь целей.",
        "premium": "Расширенный ответ: {prompt} - это инновационный продукт нового поколения, разработанный с учетом последних технологий. Он обладает широким спектром функций и возможностей, которые сделают вашу жизнь проще и комфортнее."
    }
    
    return template.get(tariff, "Неизвестный тариф").format(prompt=prompt)